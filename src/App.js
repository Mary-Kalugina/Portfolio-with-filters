const filters = ["All", "Websites", "Flayers", "Business Cards"];

const App = () => {

  const [ filterselected, changedFilter ] = useState("All");
  const [ projectsToShow, perceedProjects ] = useState(projects);

  const onSelectFilter = (filter) =>  {
     changedFilter(filter);
     filterProjects(filter);
  }

  const filterProjects = (filter) => {
    perceedProjects(filter === "All" ? projects : projects.filter((obg) => obg.category === filter));
  }

  return (
    <>
    <Toolbar
    filters={ filters }
    selected= { filterselected }
    onSelectFilter={ onSelectFilter }/>
    <ProjectList projects={ projectsToShow }/>
    </>
  );
}

export default App;
